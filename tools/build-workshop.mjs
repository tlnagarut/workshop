/* ============================================================================
 * Compiles the Workshop content into assets/workshop/workshop.js (which the
 * site reads). Run:  node tools/build-workshop.mjs
 *
 * Edit the Workshop section here — NOT in assets/workshop/workshop.js:
 *   content/workshop.yml      photos (shared)
 *   content/workshop.en.yml   English: eyebrow, title, intro, feature cards
 *   content/workshop.he.yml   Hebrew: same keys
 * ========================================================================== */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parseYaml } from "./yaml.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const contentDir = join(root, "content");
const outFile = join(root, "assets", "workshop", "workshop.js");

function load(name) {
  return parseYaml(readFileSync(join(contentDir, name), "utf8"));
}

function main() {
  const shared = load("workshop.yml");
  const en = load("workshop.en.yml");
  const he = load("workshop.he.yml");

  const nEn = (en.features || []).length;
  const nHe = (he.features || []).length;
  if (nEn !== nHe) {
    console.warn(`! ${nEn} English feature card(s) but ${nHe} Hebrew — they should match`);
  }

  const workshop = {
    photos: shared.photos || [],
    en,
    he,
  };

  const banner =
    "/* AUTO-GENERATED by tools/build-workshop.mjs — do not edit by hand.\n" +
    " * Edit the text in content/workshop.yml, content/workshop.en.yml,\n" +
    " * content/workshop.he.yml, then run:  node tools/build-workshop.mjs */\n";

  writeFileSync(outFile, banner + "\nconst WORKSHOP = " + JSON.stringify(workshop, null, 2) + ";\n");
  console.log(`Wrote Workshop (${nEn} cards, ${workshop.photos.length} photos) to assets/workshop/workshop.js`);
}

main();
