/* ============================================================================
 * Compiles the Workshop content into assets/js/workshop.js (which the
 * site reads). Run:  node tools/build-workshop.mjs
 *
 * Photos are scanned automatically from assets/workshop/ (any image files,
 * shown in filename order). Edit the text here — NOT in workshop.js:
 *   content/workshop.en.yml   English: eyebrow, title, intro, feature cards
 *   content/workshop.he.yml   Hebrew: same keys
 * ========================================================================== */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parseYaml } from "./yaml.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const contentDir = join(root, "content");
const workshopDir = join(root, "assets", "workshop");
const outFile = join(root, "assets", "js", "workshop.js");

const IMAGE_RE = /\.(jpe?g|png|webp|gif|avif|svg)$/i;

function load(name) {
  return parseYaml(readFileSync(join(contentDir, name), "utf8"));
}

// Scan assets/workshop/ for image files, in filename order.
// Skips dot-files (e.g. .DS_Store) and any non-image files.
function scanPhotos() {
  const photos = readdirSync(workshopDir)
    .filter((f) => !f.startsWith(".") && IMAGE_RE.test(f))
    .sort()
    .map((f) => `assets/workshop/${f}`);
  if (!photos.length) console.warn("! no images found in assets/workshop/");
  return photos;
}

function main() {
  const en = load("workshop.en.yml");
  const he = load("workshop.he.yml");

  const nEn = (en.features || []).length;
  const nHe = (he.features || []).length;
  if (nEn !== nHe) {
    console.warn(`! ${nEn} English feature card(s) but ${nHe} Hebrew — they should match`);
  }

  const workshop = {
    photos: scanPhotos(),
    en,
    he,
  };

  const banner =
    "/* AUTO-GENERATED by tools/build-workshop.mjs — do not edit by hand.\n" +
    " * Photos are scanned from assets/workshop/. Edit the text in\n" +
    " * content/workshop.en.yml, content/workshop.he.yml,\n" +
    " * then run:  node tools/build-workshop.mjs */\n";

  writeFileSync(outFile, banner + "\nconst WORKSHOP = " + JSON.stringify(workshop, null, 2) + ";\n");
  console.log(`Wrote Workshop (${nEn} cards, ${workshop.photos.length} photos) to assets/js/workshop.js`);
}

main();
