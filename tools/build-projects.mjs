/* ============================================================================
 * Scans assets/projects/<slug>/ folders and generates assets/js/projects.js
 * which the site reads. Run:  node tools/build-projects.mjs
 *
 * Each project folder contains:
 *   - project.md   (title, audience, description in EN/HE — see format below)
 *   - image files  (jpg/png/webp/gif/svg ...) — any names
 *
 * project.md format:
 *   ---
 *   title: English Title
 *   title_he: כותרת בעברית         (optional)
 *   audience: private              (private | business | both)
 *   order: 10                      (optional; lower shows first)
 *   cover: cover.jpg               (optional; defaults to a cover.* or first image)
 *   ---
 *   English description...
 *
 *   %%he%%
 *   Hebrew description...          (optional, after the %%he%% marker)
 * ========================================================================== */
import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const projectsDir = join(root, "assets", "projects");
const outFile = join(root, "assets", "js", "projects.js");

const IMAGE_RE = /\.(jpe?g|png|webp|gif|avif|svg)$/i;

function parseProjectMd(text) {
  const fm = {};
  let body = text;
  // Front-matter between the first pair of --- fences.
  const m = text.match(/^﻿?---\r?\n([\s\S]*?)\r?\n---\r?\n?([\s\S]*)$/);
  if (m) {
    m[1].split(/\r?\n/).forEach((line) => {
      if (!line.trim() || line.trim().startsWith("#")) return;
      const i = line.indexOf(":");
      if (i === -1) return;
      const key = line.slice(0, i).trim();
      let val = line.slice(i + 1).trim();
      val = val.replace(/^["']|["']$/g, ""); // strip optional quotes
      fm[key] = val;
    });
    body = m[2];
  }
  // Split description into EN / HE on a line that is just %%he%%
  const parts = body.split(/^\s*%%he%%\s*$/m);
  const descEn = (parts[0] || "").trim();
  const descHe = (parts[1] || "").trim();
  return { fm, descEn, descHe };
}

function buildProject(slug) {
  const dir = join(projectsDir, slug);
  const files = readdirSync(dir);

  if (!files.includes("project.md")) {
    console.warn(`! ${slug}: no project.md — skipping`);
    return null;
  }
  const { fm, descEn, descHe } = parseProjectMd(
    readFileSync(join(dir, "project.md"), "utf8")
  );

  let images = files.filter((f) => IMAGE_RE.test(f)).sort();
  if (!images.length) console.warn(`! ${slug}: no images found`);

  // Cover: explicit > a file named cover.* > first image.
  let cover = fm.cover && images.includes(fm.cover) ? fm.cover : null;
  if (!cover) cover = images.find((f) => /^cover\./i.test(f)) || images[0] || null;
  // Show the cover first in the gallery, then the rest.
  if (cover) images = [cover, ...images.filter((f) => f !== cover)];

  const base = `assets/projects/${slug}/`;
  const audience = ["private", "business", "both"].includes(fm.audience)
    ? fm.audience
    : "private";

  return {
    id: slug,
    order: Number.isFinite(parseFloat(fm.order)) ? parseFloat(fm.order) : 999,
    audience,
    cover: cover ? base + cover : "",
    images: images.map((f) => base + f),
    en: { title: fm.title || slug, description: descEn },
    he: { title: fm.title_he || "", description: descHe },
  };
}

function main() {
  let slugs = [];
  try {
    slugs = readdirSync(projectsDir).filter((name) => {
      try { return statSync(join(projectsDir, name)).isDirectory(); }
      catch { return false; }
    });
  } catch {
    console.error("No assets/projects/ folder found.");
    process.exit(1);
  }

  const projects = slugs
    .map(buildProject)
    .filter(Boolean)
    .sort((a, b) => a.order - b.order || a.id.localeCompare(b.id))
    .map(({ order, ...p }) => p); // drop the sort-only field from output

  const banner =
    "/* AUTO-GENERATED by tools/build-projects.mjs — do not edit by hand.\n" +
    " * Add projects by creating folders in assets/projects/ (see README). */\n";
  writeFileSync(outFile, banner + "window.PROJECTS = " + JSON.stringify(projects, null, 2) + ";\n");
  console.log(`Wrote ${projects.length} project(s) to assets/js/projects.js`);
}

main();
